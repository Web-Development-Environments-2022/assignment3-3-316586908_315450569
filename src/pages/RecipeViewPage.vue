import store from "../store.js";
export default {
  data() {
    return {
      recipe: null
    };
  },
  async created() {
    try {
      let response;
      // response = this.$route.params.response;

      try {
        response = await this.axios.get(
          // "https://test-for-3-2.herokuapp.com/recipes/info",
          // this.$root.store.server_domain + "/recipes/info",
          this.$root.store.server_domain + "/recipes/reviewRecipe/" + this.$route.params.recipeId,
          {
            params: { id: this.$route.params.recipeId }
          }
        );

        // console.log("response.status", response.status);
        if (response.status !== 200) this.$router.replace("/NotFound");
      } catch (error) {
        console.log("error.response.status", error.response.status);
        this.$router.replace("/NotFound");
        return;
      }

      let {
        analyzedInstructions,
        instructions,
        ingredients,
        popularity,
        readyInMinutes,
        image,
        title,
        vegan,
        vegetarian,
        glutenFree,
        seen,
        favorite,
        servings
      } = response.data.recipe;
      let _instructions = null;
      if (analyzedInstructions)
      {
        _instructions = analyzedInstructions
          .map((fstep) => {
            if (fstep.steps[0].step.length != 0){
              fstep.steps[0].step = fstep.name + ' ' + fstep.steps[0].step;
              return fstep.steps;
            }
          })
          .reduce((a, b) => [...a, ...b], []);
      }

      let _recipe = {
        instructions,
        _instructions,
        analyzedInstructions,
        ingredients,
        popularity,
        readyInMinutes,
        image,
        title,
        vegan,
        vegetarian,
        glutenFree,
        seen,
        favorite,
        servings
      };

      this.recipe = _recipe;
    } catch (error) {
      console.log(error);
    }
  },
  methods:{
    async Save(){
      try{
        await this.axios.post(this.$root.store.server_domain + '/users/favorites',
        {
            recipeId: this.$route.params.recipeId
        });
        this.recipe.favorite = true;
      }
      catch (error){
        console.log(error);
      }
    }
  }
};
</script>

